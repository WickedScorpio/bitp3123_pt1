package samples.demo;

public class Location {

	private String tempat;
	private String negara;
	
	public String getTempat() {
		return tempat;
	}
	public void setTempat(String tempat) {
		this.tempat = tempat;
	}
	public String getNegara() {
		return negara;
	}
	public void setNegara(String negara) {
		this.negara = negara;
	}
	
}

 
